"""Place the single-site factorial cells on the rate–distortion plane.

The joint codec payloads price one model transmitting all 8 sites; the
single-site cells (bsf = block/single-site, sae = scalar/single-site) are
8 independent per-site models, so the system rate is the *sum* of per-site
rates and the system distortion is the sq_tot-weighted pool of per-site
FVUs. Exact eval-split weights come from `eval_site_sq_tot.json`
(generated by a CPU pass over the store; both gauges — the whitened
weights are deep-tilted 7.3x by shrinkage-whitener retained power, the
renormed weights are uniform within 2%).

Reads compact codec payloads from ``data/evidence/`` and updates
``single_site_placement.json`` there.
"""
from __future__ import annotations

import json
from pathlib import Path

DATA = Path(__file__).resolve().parents[2] / "data" / "evidence"
SITES = (9, 12, 15, 18, 21, 24, 27, 30)


def load(name: str) -> dict:
    return json.loads((DATA / f"{name}.json").read_text())


def system(prefix: str, weights: dict[str, list[float]]) -> dict:
    """Aggregate 8 single-site payloads into one system point per q."""
    per_site = {s: load(f"{prefix}{s}") for s in SITES}
    out: dict = {"per_site_files": [f"{prefix}{s}" for s in SITES], "points": {}}
    support = sum(p["results"]["support_bits_per_token"] for p in per_site.values())
    out["support_bits_per_token"] = support
    for q in ("4", "6", "8"):
        fvu = {s: per_site[s]["results"]["points"][q]["fvu_pooled"] for s in SITES}
        rate = sum(
            per_site[s]["results"]["points"][q]["rate_bits_per_token"] for s in SITES
        )
        amp = sum(
            per_site[s]["results"]["points"][q]["amplitude_bits_per_token"]
            for s in SITES
        )
        out["points"][q] = {
            "fvu_per_site": [fvu[s] for s in SITES],
            "fvu_pooled_whitened": sum(
                w * fvu[s] for w, s in zip(weights["w_whitened"], SITES)
            ),
            "fvu_pooled_renorm": sum(
                w * fvu[s] for w, s in zip(weights["w_renorm"], SITES)
            ),
            "rate_bits_per_token": rate,
            "amplitude_bits_per_token": amp,
        }
    return out


def joint_point(name: str, q: str) -> tuple[float, float, list[float]]:
    p = load(name)
    pt = p["results"]["points"][q]
    return pt["fvu_pooled"], pt["rate_bits_per_token"], pt["fvu_per_site"]


def main() -> None:
    weights = load("eval_site_sq_tot")
    placement = {
        "weights_source": "eval_site_sq_tot.json (exact eval-split pass)",
        "bsf_system": system("ss_bsf_seed0_site", weights),
        "sae_system": system("ss_sae_seed0_site", weights),
    }

    q = "6"
    print(f"=== single-site system placement (q={q}) ===\n")
    header = f"{'system / joint':34s} {'FVU(pool)':>10s} {'bits/tok':>9s}"
    print(header)
    for label, sysd in (("bsf 8x single-site", placement["bsf_system"]),
                        ("sae 8x single-site", placement["sae_system"])):
        pt = sysd["points"][q]
        print(f"{label + ' [whitened pool]':34s} "
              f"{pt['fvu_pooled_whitened']:10.4f} {pt['rate_bits_per_token']:9.1f}")
        print(f"{label + ' [renorm pool]':34s} "
              f"{pt['fvu_pooled_renorm']:10.4f} {pt['rate_bits_per_token']:9.1f}")

    joints = {
        "joint bsc k32 (primary, lam0)": "f_bsc_lam0_k32",
        "joint bsc k32 (renorm, lam0)": "f_bsc_lam0_k32_renorm",
        "joint scalar k128 (lam0)": "rd_scalar",
    }
    for label, name in joints.items():
        fvu, rate, _ = joint_point(name, q)
        print(f"{label:34s} {fvu:10.4f} {rate:9.1f}")

    print("\n=== per-site FVU profiles (q=6) ===\n")
    prof: dict[str, list] = {"site": list(SITES)}
    prof["bsf(single)"] = placement["bsf_system"]["points"][q]["fvu_per_site"]
    prof["sae(single)"] = placement["sae_system"]["points"][q]["fvu_per_site"]
    for label, name in joints.items():
        _, _, per = joint_point(name, q)
        prof[name] = per
    cols = list(prof.keys())
    print(" ".join(f"{c:>22s}" for c in cols))
    for i in range(len(SITES)):
        print(" ".join(
            f"{prof[c][i]:22.4f}" if c != "site" else f"{prof[c][i]:>22d}"
            for c in cols
        ))

    for label, name in joints.items():
        _, rate, _ = joint_point(name, "6")
        sysd = (placement["bsf_system"] if "bsc" in name
                else placement["sae_system"])
        cut = sysd["points"]["6"]["rate_bits_per_token"] / rate
        print(f"\ntying rate cut vs {label}: {cut:.2f}x")

    (DATA / "single_site_placement.json").write_text(
        json.dumps(placement, indent=1)
    )
    print("\nwrote", DATA / "single_site_placement.json")


if __name__ == "__main__":
    main()
